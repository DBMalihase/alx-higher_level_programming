#!/usr/bin/python3
def add(a, b):
    """
    Args:
    a: first int
    b: second int
    Returns:
    a + b"""
    return (a + b)

def sub(a, b):
    """
    Args:
    a: first int
    b: second int
    Returns: a - b"""
    return(a - b)

def mul(a, b):
    """Args:
    a: first int
    b: second int
    Returns:
    a * b"""
    return(a * b)

def div(a, b):
    """Args:
    a: first int
    b: second int
    Returns: a / b"""
    return int(a / b)
